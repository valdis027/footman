package Footman;

public class club {
    private int id_club;
    private String name;
    private int id_coach;
    private int id_champ;


    public int id_club() {
        return id_club;
    }

    public void set_id_club(int id_club) {
        this.id_club = id_club;
    }

    public String get_name() {
        return name;
    }

    public void set_name(String name) {
        this.name = name;
    }

    public int id_coach() {
        return id_coach;
    }

    public void set_id_coach(int id_coach) {
        this.id_coach = id_coach;
    }

    public int id_champ() {
        return id_champ;
    }

    public void set_id_champ(int id_champ) {
        this.id_champ = id_champ;
    }
}
